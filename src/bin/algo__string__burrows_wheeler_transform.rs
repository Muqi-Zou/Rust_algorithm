#![allow(warnings)]
#![allow(clippy::all)]

pub use the_algorithms_rust::string::burrows_wheeler_transform as source;
pub use source::*;

fn main() {
    let mut failures = 0usize;
    let mut executed = 0usize;
    const TESTS: &[(&str, fn())] = &[
        ("stand_alone_function", source::tests::stand_alone_function as fn()),
        ("basic_characters", source::tests::basic_characters as fn()),
        ("special_characters", source::tests::special_characters as fn()),
        ("empty", source::tests::empty as fn()),
    ];
    for (name, test) in TESTS {
        let result = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| test()));
        executed += 1;
        match result {
            Ok(_) => println!("[ok] {}::tests::{}", core::module_path!(), name),
            Err(_) => {
                println!("[failed] {}::tests::{}", core::module_path!(), name);
                failures += 1;
            }
        }
    }
    if executed == 0 {
        println!("No tests discovered.");
    }
    if failures > 0 {
        std::process::exit(1);
    }
}
